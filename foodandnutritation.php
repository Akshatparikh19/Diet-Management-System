      <h3><b>Nutritional Deficiencies
</b></h3>
    </div>
    <div class="w3-container">
      <p>Even if you are getting enough to eat, if you are not eating a balanced diet, you may still be at risk for certain nutritional deficiencies. Also, you may have nutritional deficiencies due to certain health or life conditions, such as pregnancy, or certain medications you may be taking, such as high blood pressure medications. People who have had intestinal diseases or had sections of intestines removed due to disease or weight loss surgery also may be at risk for vitamin deficiencies. Alcoholics are also at high risk of having nutritional deficiencies.</p>

<p>One of the most common nutritional deficiencies is iron deficiency anemia. Your blood cells need iron in order to supply your body with oxygen, and if you don’t have enough iron, your blood will not function properly. Other nutritional deficiencies that can affect your blood cells include low levels of vitamin B12, folate, or vitamin C.</p>

<p>Vitamin D deficiency may affect the health of your bones, making it difficult for you to absorb and use calcium (another mineral that you may not be getting enough of). Although you can get vitamin D by going out in the sun, many people with concerns about skin cancer may end up with low levels of vitamin D by not getting enough sun.</p>

<p><b>Other nutritional deficiencies include:</b>
<br><br>
->		beriberi: low levels of vitamin B1 (found in cereal husks)<br>
->		ariboflavinosis: low levels of vitamin B2<br>
->		pellagra: low levels of vitamin B3<br>
->		paraesthesia: low levels of vitamin B5 leading to a “pins and needles” feeling<br>
->		biotin deficiency: low levels of vitamin B7, which can be common in pregnancy<br>
->		hypocobalaminemia: low levels of B12<br>
->		night blindness: low levels of Vitamin A<br>
->		scurvy: low levels of vitamin C<br>
->		rickets: severe vitamin D and/or calcium deficiency<br>
->		vitamin K deficiency<br>
->		magnesium deficiency: occurs with certain medications and medical problems<br>
->		potassium deficiency: occurs with certain medications and medical problems</p>
<p>Eating a balanced diet can help prevent these conditions. Vitamin supplements may be necessary for certain people, such as pregnant or nursing mothers and people with intestinal conditions.</p>
