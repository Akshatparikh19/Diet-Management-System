      <h3><b>Diseases and Conditions Influenced by Nutrition</b></h3>
      
    </div>

    <div class="w3-container"><p>
     Many health conditions are caused and/or affected by food and nutrition. Some are directly caused by food, such as “food poisoning” or bacterial infections from contaminated food. Some people can have severe allergies to foods like peanuts, shellfish, or wheat (celiac disease). Gastrointestinal ailments—such as irritable bowel syndrome, ulcerative colitis, and gastroesophageal reflux disease (GERD)—are also directly affected by the consumption of food.</p>

<p>For other diseases and conditions, the type or quantity of food can influence the progress of the disease. Diabetes mellitus, for example, which results in the inability of the body to regulate blood sugar, is drastically affected by the types and quantities of food eaten. Carbohydrate intake has to be carefully monitored if you suffer from diabetes, or blood sugar can rise to dangerous levels.</p>
<p><b> Other conditions affected by food and nutrition include:</b><br><br>
->hypertension: Salt intake affects blood pressure.<br>
->heart disease/high cholesterol: Fatty foods and partial hydrogenated oils can create plaque in arteries.<br>
->osteoporosis: Low calcium, low vitamin D and excess fat can result in fragile bones.<br>
->certain cancers: A poor diet and obesity are associated with increased risk of breast, colon, endometrial, esophageal, and kidney cancers.</p>
<p>Your food choices and nutritional status can influence your overall health over the entire course of your life.</p>
